	mov r0, # 65
	mov r1, # 7
	ldr r2, [r3]
	bl DrawNum
	 
	ldr r0, =LeftRoadCarsArray	//Update Cars  states
	ldr r1, =CarsArraysSize
	ldr r1, [r1]
	bl UpdateSpritesStates
	
	ldr r0, =MiddleRoadCarsArray	//Update Cars  states
