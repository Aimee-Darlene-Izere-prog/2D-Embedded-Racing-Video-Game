	ldr r1, [r1]
	bl UpdateSpritesStates
	
	ldr r0, =RightRoadCarsArray	//Update Cars  states
	ldr r1, =CarsArraysSize
	ldr r1, [r1]
	bl UpdateSpritesStates
	
	ldr r0, =Power_Ups	//Update Cars  states
	ldr r1, =PowerUpsArraySize
	ldr r1, [r1]
	bl UpdateSpritesStates
	
	
	ldr r0, =Player	//Update The player's states
	bl MovePlayer
	
	ldr r0, =LeftRoadCarsArray	// Check if there is any collision before drawing
	bl CheckCollisions
	
	ldr r0, =MiddleRoadCarsArray	// Check if there is any collision before drawing
	bl CheckCollisions
	
	ldr r0, =RightRoadCarsArray	// Check if there is any collision before drawing
	bl CheckCollisions
	
	ldr r0, =Power_Ups 
	bl PowerUpCollision
	
	ldr r0, =LeftRoadCarsArray	// Draw left cars
	ldr r1, =CarsArraysSize
	ldr r1, [r1]
	bl DrawSprites
	
	ldr r0, =MiddleRoadCarsArray	// Draw middle cars
	ldr r1, =CarsArraysSize
	ldr r1, [r1]
	bl DrawSprites
	
	ldr r0, =RightRoadCarsArray	// Draw right cars
