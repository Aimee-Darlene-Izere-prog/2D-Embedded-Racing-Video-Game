	// Clear the VGA screen and text buffer
	// I use Double Buffering to clear VGA
	mov r0, #0x0000			// Black background
	mov r1, r8		// r1: pointer to the draw_buffer
	bl ClearVGA
	bl ClearTextBuffer
	
	bl CheckUserInput 		// get User input
	
	//	Draw Heath Status Message
	mov r0, #60		
	mov r1, #5		 
	ldr r2, =Health_msg	 
