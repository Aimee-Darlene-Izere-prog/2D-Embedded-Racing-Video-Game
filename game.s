	sub r5, r5, #1		// index++
	add r4, r4, #16		// Move to the next sprite
	b UpdateSpritesStates_Body
	
UpdateSpritesStates_Done:
	pop {r4, r5, pc}

//
// DrawSprites routine below
// Draw Sprites at their given locations
.align 2
DrawSprites:  
	push {r4, r5, r6, r7, lr}	 
	mov r4, r0				// r4 = sprite structure
	mov r7, r1				// r7 = size of the array
	mov r5, #0				// index
	
	b DrawSprites_Cond
	
DrawSprites_Body:
	// Load sprite fields 
	ldr r0, [r4, #sprite_DATAPTR]		//  r0 = sprite_ptr = pixmap_ptr
	cmp r0, #0
	beq Skip_DrawSprites
	
	ldrsh r1, [r4, #sprite_xPos]		//  r1 = sprite_x = pmap_x
	ldrsh r2, [r4, #sprite_yPos]		//  r2 = sprite_y = pmap_y
	
	mov r3, r8
	bl BitBlit

Skip_DrawSprites:
	// Update Indexes
	add r4, r4, #16	// Move to the next sprite
	add r5, r5, #1	// index++
	
DrawSprites_Cond:
	cmp r5, r7	// r5 is the size of the given sprite array
	blt	DrawSprites_Body

pop {r4, r5, r6, r7, pc}

//
// MovePlayer routine below
//
MovePlayer:
	// Input r0: pointer to sprite
	push {r4, r5, r6, r7, r8, r9, lr}
	
	// Load position and velocity
	ldrsh r4, [r0, #sprite_xPos]
	ldrsh r1, [r0, #sprite_xVel]
	 
	add r4, r4, r1	// r4: new_xpos

	ldrsh r5, [r0, #sprite_yPos]
	ldrsh r9, [r0, #sprite_jumpCounter]	
	cmp r9, #0
	beq  MoveYVel
	
	// Allow the player to Jump
	ldrsh r9, [r0, #sprite_jumpCounter]
	cmp r9, #0
	beq MoveYVel
	
	// Apply gravity
	ldrsh r2, [r0, #sprite_yVel]
	add r2, r2, #1		// r2: gavity
	add r5, r5, r2		// r5: yPos + y_Vel
	
	strh r2, [r0, #sprite_yVel]
	
	// Land after jumping
	cmp r5, #120
	ble AfterLanding
	
	mov r5, #120
	mov r2, #0
	mov r9, #0
	
	strh r2, [r0, #sprite_yVel]
	strh r9, [r0, #sprite_jumpCounter]
	b AfterLanding
	
MoveYVel: 	
	ldrsh r2, [r0, #sprite_yVel]
	add r5, r5, r2	// r5: new_ypos

AfterLanding: 
	// Load height and Width
	ldr r3,  [r0, #sprite_DATAPTR]
	ldrh r6, [r3, #PIXMAP_WIDTH]
	ldrh r7, [r3, #PIXMAP_HEIGHT]

	// if (new_xpos < 0), update new_xpos = 0
	cmp r4, #0
	bge RightEdge	// RightEdge branch checks if x is < 320. 320 is the VGA width
	mov r4, #0		// update new_xpos = 0

RightEdge: // Check Right Screen Boundaries
	// if (new_xpos + width > 320)
	add r8, r4, r6
	cmp r8, #320
	ble TopEdge	// x is within the bound. Now USC1 clamps y
	
	rsb r4, r6, #320
	
TopEdge:// Check Top Screen Boundaries
	// Clamp y
	cmp r5, #0
	bge BottomEdge	// BottomEdge checks if y_pos is off limits
	
	mov r5, #0

BottomEdge:// Check Bottom Screen Boundaries
	// if (new_ypos + height < 0)
	add r8, r5, r7
	cmp r8, #240
	ble StoreNewPos
	rsb r5, r7, #240
	
StoreNewPos: 
	strh r4, [r0, #sprite_xPos]
	strh r5, [r0, #sprite_yPos]
	
	pop {r4, r5, r6, r7, r8, r9, pc}

//
//	UpdateSprite routine below
//	
UpdateSprite: // Keeps track of the positions of the sprites as their change positions
	push {r4, r5, lr}
	
	// Since cars only moves vertically, I need to load only yPos and yVel
	ldrsh r5, [r0, #6]		// For reference, #6 because .EQU sprite_yPos, 6
	ldrsh r2, [r0, #10]		// #10 because .EQU sprite_yVel, 10
 
 	// New yPos = yPos + yvel
	add r5, r5, r2	// r5: New yPos
	
	// Handle Boundaries
	ldr r1, =-120
	cmp r5, #240
	movgt r5, r1
	
	// Store New yPos
	strh r5, [r0, #6]
	
	pop {r4, r5, pc}
	
//
// PowerUpCollision
//
.align 2
PowerUpCollision: // Checks if the player was able to catch the power up
	push {r4, r5, r6, r7, lr}
	ldr r4, =Power_Ups	
	ldr r7, =PowerUpsArraySize
	ldr r7, [r7]
	mov r6, #0		// i
	b PowerUpCond
	
PowerUpBody:
	lsl r1, r6, #4		// Each sprite in the array is 16 bits
	add r1, r4, r1
	
	ldr r0, =Player
	bl HandlePowerUpCollision
	
	add r6, r6, #1	//  i++	
PowerUpCond:
	cmp r6, r7
	blt PowerUpBody
	
	pop {r4, r5, r6, r7, pc}
	
//
// HandlePowerUpCollision Routine below
//
.align 2
HandlePowerUpCollision: 
	push {r4-r10, lr}
	mov r8, r0		// Player ptr
	mov r9, r1		// PowerUp ptr
	
	// Load Player Positions
	ldrsh r2, [r8, #sprite_xPos]
	ldrsh r3, [r8, #sprite_yPos]
	
	// Load PowerUP locations
	ldrsh r4, [r9, #sprite_xPos]
	ldrsh r5, [r9, #sprite_yPos]
	
	// calculate the Distance between player and the PowerUp
	sub r6, r2, r4
AbsoluteValue_X:
	cmp r6, #0
	rsblt r6, r6, #0
	
	cmp r6, #16 	// #16: width of the collision
	bge NoPowerUpCollison
	
	sub r6, r3, r5
AbsoluteValue_Y:
	cmp r6, #0
	rsblt r6, r6, #0
	
	cmp r6, #16 	// #16: heightof the collision
	bge NoPowerUpCollison
	
	// Player and PowerUp collided
	mov r0, r8
	// Handle Collision by incrementing health/ Power Ups
	bl IncrementPowerUps
	
	// Remove PowerUp offscreen
	mov r2, #-200
	strh r2, [r9, #sprite_yPos]
	 	
NoPowerUpCollison: 	
	pop {r4-r10, pc}

//
// IncrementPowerUps Routine Below
// 
.align 2	
IncrementPowerUps:	// Increment the number of powerUps the player has by 1
	push {r4, lr}
	
	ldr r0, =PlayerPowerUps
	ldr r1, [r0]
	
	ldr r2, =MaxPowerUps
	ldr r2, [r2]
	
	cmp r1, r2
	bge MaxReached
	
	add r1, r1, #1
	str r1, [r0]
	
MaxReached: 	
	pop {r4, pc}
	

//
// CheckCollisions Routine Below
//
.align 2
CheckCollisions:	// Check if the player collides with a car 
	push {r4, r5, r6, r7, lr}
	
	ldr r5, =Player	 
	mov r4, r0		 
	ldr r7, =CarsArraysSize
	ldr r7, [r7]
	mov r6, #0		// i
	
OuterLoop:
	add r1, r4, r6, lsl #4
	mov r0, r5
	bl Check_Collision
	
	add r6, r6, #1	//  i++
	
OuterCond:
	cmp r6, r7
	blt OuterLoop
	
	pop {r4, r5, r6, r7, pc}
	
//
// GetCarsArrayPtr routine below
// 
.align 2
GetCarsArrayPtr: // get pointers to all the Car Arrays 
	push {r4, r5, r6, r7, lr}
	
	// Start with left cars
	ldr r5, =CarsArraysSize
	ldr r5, [r5]
	ldr r4, =LeftRoadCarsArray
	
LeftRoadCarsLoop:
	subs r5, r5, #1		// r5: Number of cars left in the array
	blt LeftLoopDone
	
	ldr r0, =Player
	add r1, r4, r5, lsl #4
	bl Check_Collision
	b LeftRoadCarsLoop
	
LeftLoopDone:
	// Left cars done, check middle cars
	ldr r5, =CarsArraysSize
	ldr r5, [r5]
	ldr r4, =MiddleRoadCarsArray
	
MiddleRoadCarsLoop:
	subs r5, r5, #1		
	blt MiddleLoopDone
	
	ldr r0, =Player
	add r1, r4, r5, lsl #4
	bl Check_Collision
	b MiddleRoadCarsLoop
	
MiddleLoopDone:
	// Middle cars done, check right cars
	ldr r5, =CarsArraysSize
	ldr r5, [r5]
	ldr r4, =RightRoadCarsArray
	
RightRoadCarsLoop:
	subs r5, r5, #1		
	blt RightLoopDone
	
	ldr r0, =Player
	add r1, r4, r5, lsl #4
	bl Check_Collision
	b RightRoadCarsLoop
	
RightLoopDone:
	pop {r4, r5, r6, r7, pc}


//
// Check_Collision helper Routine Below
//
.align 2	
Check_Collision:
	push {r4-r12, lr}
	
	mov r10, r0		// player pointer
	mov r11, r1		// Car pointer
	
	ldr r7, [r10, #sprite_DATAPTR]
	ldr r8, [r11, #sprite_DATAPTR]
	
	// Check for Null pointers
	cmp r7, #0
	beq NoCollision
	cmp r8, #0
	beq NoCollision
	
	
	// Load player and Car positions
	ldrsh r2, [r10, #sprite_xPos]
	ldrsh r3, [r10, #sprite_yPos]
	
	ldrsh r4, [r11, #sprite_xPos]
	ldrsh r5, [r11, #sprite_yPos]
	
	// abs(xA - xB)
	sub r6, r2, r4
	cmp r6, #0
	rsblt r6, r6, #0
	
	// Check for collisions
	cmp r6, #12
	bge NoCollision
	
	// abs(yA - yB)
	sub r6, r3, r5
	cmp r6, #0
	rsblt r6, r6, #0
	
	// Check for collisions
	cmp r6, #12
	bge NoCollision
	
	// Collision happened, Check if the player has power ups
	// If so, substract power ups, but don't let the player die
	bl CheckHealthStatus
	cmp r0, #0	// r0 == 0 if the player does not have power ups. r0 == 1 if the player has power ups
	beq	AfterDeath
	
	// player has power up
	mov r2, #-200
	strh r2, [r11, #sprite_yPos]
	
	// Use the power up of the player
	 
	ldr r3, =PlayerPowerUps
	ldr r4, [r3]
	subs r4, r4, #1
	movlt r4, #0
	str r4, [r3]
	
	b NoCollision

AfterDeath: 
	// Handle Collision 
	ldr r8, =0xc8000000		// display Buffer
	
	// Black bacground
	mov r0, #0x0000
	mov r1, r8
	bl ClearVGA
	
	// Display Explosion Sprite
	ldr r0, =Fire_Explosion
	ldr r1, =Player
	ldrsh r2, [r1, #sprite_xPos]
	ldrsh r3, [r1, #sprite_yPos]
	
	// Clamp explosion sprite before drawing it
	cmp r2, #0
	movlt r2, #0
	cmp r2, #280
	movgt r2, #280
	cmp r3, #0
	movlt r3, #0
	cmp r3, #200
	movgt r3, #200
	
	strh r2, [r0, #sprite_xPos]
	strh r3, [r0, #sprite_yPos]
	
	// Display Explosion
	ldr r0, =Fire_Explosion
	mov r1, #1
	bl DrawSprites
	
	//	Draw Collision Message
	mov r0, #40		// r0 = x
	mov r1, #30		// r1 = y
	ldr r2, =collision_msg	// r2 = string
	bl DrawStr
	
	// Swap buffers
	ldr r0, =0xff203020
	ldr r1, =0xc8000000
	str r1, [r0, #4]
	mov r2, #1
	str r2, [r0, #0]

WaitForSwapEnd:
	ldr r1, [r0, #12]
	tst r1, #1
	bne WaitForSwapEnd
	
	// End game if there is Collision
GameOver:
	b GameOver
	
NoCollision: 	
	pop {r4-r12, pc}
	
//
// Check if Player has life savings 
//
CheckHealthStatus:
	push {lr}
	ldr r0, =PlayerPowerUps
	ldr r1, [r0]
	
	cmp r1, #0
	ble NoPowerUps
	
	mov r0, #1
	pop {pc}
	
NoPowerUps:
	mov r0, #0
	pop {pc}
	
//
//CheckUserInput routine below
//
.align 2
CheckUserInput: // Through reading the push buttons
	push {r4, r5, r6, lr}
	
	// Load current buttons
	ldr r4, =PUSH_BUTTONS
	ldr r5, [r4]	// r5: current buttons
	
	// Load previous buttons
	ldr r6, =previousButtons
	ldr r0, [r6]	// r0: previous buttons
	
	// Pressing Button 1 and Button 2 allows the player to jump
	and r1, r5, #6		// r1: Button 1 and 2
	cmp r1, #6
	bne CheckOtherButtons
	
	and r2, r0, #6
	cmp r2, #6
	beq CheckOtherButtons	// Not pressed, ignore
	
	ldr r2, =Player
	ldr r3, [r2, #sprite_jumpCounter]
	cmp r3, #0
	bne CheckOtherButtons
	
	mov r3, #1
	strh r3, [r2, #sprite_jumpCounter]
	
	mov r3, #-10
	strh r3, [r2, #sprite_yVel]
	
CheckOtherButtons: 	
	// Button 0: Turns the player to the right
	tst r5, #1
	beq TurnLeft
	
	ldr r2, =Player
	mov r3, #1
	strh r3, [r2, #sprite_xVel]
	b UpOrDown
	
TurnLeft: 
	tst r5, #8
	beq StopXVel
	
	ldr r2, =Player
	mov r3, #-1
	strh r3, [r2, #sprite_xVel]
	b UpOrDown
	
StopXVel: 
	ldr r2, =Player
	mov r3, #0
	strh r3, [r2, #sprite_xVel]

UpOrDown: 
	ldr r2, =Player
	ldrh r3, [r2, #sprite_jumpCounter]
	cmp r3, #0
	bne StoreButtons
	
	tst r5, #2
	beq MoveDown
	
	//ldr r2, =Player
	mov r3, #-1
	strh r3, [r2, #sprite_yVel]
	b StoreButtons

MoveDown: 
	tst r5, #4
	beq StopYVel
	
	mov r3, #1
	strh r3, [r2, #sprite_yVel]
	b StoreButtons

StopYVel: 
	mov r3, #0
	strh r3, [r2, #sprite_yVel]

StoreButtons:
	str r5, [r6]	 
	pop {r4, r5, r6, pc}


// ClearTextBuffer routine below
.align 2
ClearTextBuffer:
	mov r2, #0x20
	mov r0, #0 		// r0 = y
	b OutsideLoop_cond

OutsideLoop_body:
	mov r1, #0		// r1 = x
	b InsideLoop_cond
	
InsideLoop_body:
	lsl r3, r0, #7
	add r3, r3, r1
	add r3, r3, #TEXT_BUFFER
	strb r2, [r3]
	add r1, r1, #1	// x++
	
InsideLoop_cond:
	cmp r1, #80 	// 80 is Character buffer width
	blo InsideLoop_body
	add r0, r0, #1 // y++
	
OutsideLoop_cond:	
	cmp r0, #60		// 60 is Character buffer height
	blo OutsideLoop_body
bx lr 			// return
	
//
//	ClearVGA Routine Below
//
 
.align 2
ClearVGA: // Argument r0: color, r1: draw_buffer base
	push {r4-r6, lr}
	mov r4, #240
	
ClearVGA_Outer_Loop:	// Clear y
	mov r5, #320
	mov r6, r1  // r6: buffer base

ClearVGA_Inner_Loop:	// clear X
	strh r0, [r6]
	add r6, r6, #2	// Next y pixel
	subs r5, r5, #1
	bne ClearVGA_Inner_Loop
	
	add r1, r1, #1024
	subs r4, r4, #1
	bne ClearVGA_Outer_Loop
	pop {r4-r6, pc}

//
// BitBlit Routine Below
//
.align 2 
BitBlit:
	push {r4-r12, lr}
	mov r4, r0			// r0 = pixmap_ptr
	mov r5, r1		    // r1 = x
	mov r6, r2			// r2 = y
	mov r11, r3			// r3 = draw_buffer
	
	// Load pixmap fields 
	ldrb r0, [r4, #0]		//  r0 = pmap->width Low
	ldrb r12, [r4, #1]		//  r0 = pmap->width High
	orr r0, r0, r12, lsl #8	// r0: width
	
	ldrb r1, [r4, #2]		//  r1 = pmap->height Low
	ldrb r12, [r4, #3]		//  r1 = pmap->height High
	orr r1, r1, r12, lsl #8	// r1 = Height
	
	ldrb r2, [r4, #4]	//  r2 = pmap->transparency Low
	ldrb r12, [r4, #5]	//  r2 = pmap->transparency High
	orr r2, r2, r12, lsl #8
	 
	 add r3, r4, #6		// PIXMAP_DATA
	 mov r9, #0
	 
BitBlit_outerCond: 
	cmp r9, r1
	bge Blit_Done
	mov r10, #0
	 
BitBlit_InnerBody:
	cmp r10, r0
	bge BitBlit_outerBodyDone 
	
	ldrb r4, [r3], #1	
	ldrb r12, [r3], #1
	orr r4, r4, r12, lsl #8	// color = *pixelptr 
	
	cmp r4, r2			// Check if color != transparency
	beq DoNotDraw
	
	add r7, r5, r10	// screen_x = start_x + j
	add r8, r6, r9		// screen_y = start_y + i
	
	// Check if x is inside screen bounds. Clip x
	cmp r7, #0
	blt DoNotDraw
	cmp r7, #320
	bge DoNotDraw
	
	// Check if y inside screen bounds. Clip Y
	cmp r8, #0
	blt DoNotDraw
	cmp r8, #240
	bge DoNotDraw
	
	// compute VGA address
	lsl r12, r8, #10
	add r12, r12, r7, lsl #1
	add r12, r12, r11	// r11: Draw_buffer
	
	strh r4, [r12]	//strh color
	
DoNotDraw: 	
	add r10, r10, #1	// j++
	b BitBlit_InnerBody
 	
BitBlit_outerBodyDone:
	add r9, r9, #1 		// i++
	b BitBlit_outerCond
	
Blit_Done:	
	pop {r4-r12, pc}
 
//
//  DrawStr Routine Below
//
.align 2
DrawStr:
	push {r4, r5, r6, r7, lr}
	mov r4, r0		// r4 = x
	mov r5, r1		// r5 = y
	
while_loop:
	ldrb r6, [r2]	// r6 = n 
	cmp r6, #0
	beq break_while_loop		// if r6 == 0, we've reached the end of the string. Then, break out of the while loop
	
	// Check if x and y are valid coordinates
	// character buffer coordinates : 80 * 60 characters
	cmp r4, #80
	bhs skip_drawing	// if x >= 80, do not draw
	cmp r5, #60
	bhs skip_drawing	// if y >= 60, do not draw
	
	// calculate the location of where to draw the string in the character buffer VGA display
	lsl r7, r5, #7 				// logic shift applied to r7 to the left by 7 bits
	add r7, r7, r4
	add r7, r7, #TEXT_BUFFER
	strb r6, [r7]				// store the characters
	
skip_drawing:
	add r4, r4, #1				// x++
	add r2, r2, #1				// s++, goes to the next character
	b while_loop				// keeps drawing until n == 0 (null that termites the string n)
	
break_while_loop:
	pop {r4, r5, r6, r7, pc}	 

//
// DrawNum Routine Below
//
.align 2
DrawNum:
	push {r4, r5, r6, r7, lr}	//Prologue
	
	mov r4, r0 		// r4 = x
	mov r5, r1 		// r5 = y
	mov r6, r2		// r6 = n 
	
	cmp r6, #0
	bge n_is_positive	// n is already positive
	rsb r6, r6, #0		// r6 = abs(n)                 	
	
n_is_positive: // Do nothing because n > 0
 
DrawNum_loop: 
 	 mov r0, r6		// r0 = remainder and r6 contains abs(n) 
	 mov r1, #0		// r1 = quotient

Division_loop:
	cmp r0, #10
	blt Division_done	// if r0 < 10, then the division is done
	sub r0, r0, #10 	// remainder = remainder - divisor
	add r1, r1, #1 		// quotient = quotient + 1
	b Division_loop		// keeps looping until r0 < 10
	
Division_done:	
	// Check if x and y are valid coordinates
	// character buffer coordinates : 80 * 60 characters
	cmp r4, #CHAR_WIDTH
	bhs skip_Numdrawing		// if r4 >= 80, do not draw
	cmp r5, #CHAR_HEIGHT
	bhs skip_Numdrawing		// Also, if r4 >= 80, do not draw
	
	
	//calculating the location of where to draw the digits from n in the character buffer VGA display
	add r0, r0, #0x30		// r0 = remainder
	lsl r7, r5, #7 
	add r7, r7, r4
	add r7, r7, #CHARBUF
	strb r0, [r7] 			// store digits
	
skip_Numdrawing:
	sub r4, r4, #1 			// x--, to be able to represent the remainders not backwards
	mov r6, r1				// r6 = n = quotient
	
	cmp r6, #0
	bne DrawNum_loop		// if quotient != 0, keep drawing
	
	// Do this if n is negative
	cmp r2, #0
	bge DrawNum_done		// if n<= 0, then we've finished drawing all the numbers 
	
	// Else, it is a negative number and we will draw it with a "-"
	// Check if x and y are valid coordinates
	// character buffer coordinates : 80 * 60 characters
	cmp r4, #CHAR_WIDTH
	bhs DrawNum_done		// if r4 >= 80, then we've finished drawing all the numbers
	cmp r5, #CHAR_HEIGHT
	bhs DrawNum_done		// if r4 >= 60, then we've finished drawing all the numbers
	
	//calculating the location of where to draw the digits from n in the character buffer VGA display
	lsl r7, r5, #7 
	add r7, r7, r4
	add r7, r7, #CHARBUF
	mov r0, #'-'
	strb r0, [r7] // store digits after the '-' sign
	
DrawNum_done:
	pop {r4, r5, r6, r7, pc}	
